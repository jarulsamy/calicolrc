	public class Shape
	{
	   /// Abstract base class for all the drawing shapes.
		public Shape ()
		{
		}
	}

	public class CompoundShape : Shape
	{
		Shape[] shapes = null;

		public CompoundShape (params Shape [] shapes) : base()
		{
			this.shapes = shapes;
		}
	}

	public class Element : CompoundShape
	{
	   /// Base class for graph nodes and edges.

		public Element (params Shape [] shapes) : base(shapes)
		{
		}

		public virtual Url get_url (double x, double y)
		{
			return null;
		}
	}

	public class Node : Element
	{
		double x;
		double y;
		double x1;
		double y1;
		double x2;
		double y2;
		string url;

		public Node (double x, double y, double w, double h, Shape [] shapes, string url): base(shapes)
		{
			this.x = x;
			this.y = y;
	       
			this.x1 = x - 0.5 * w;
			this.y1 = y - 0.5 * h;
			this.x2 = x + 0.5 * w;
			this.y2 = y + 0.5 * h;

			this.url = url;
		}

		public bool is_inside (double x, double y)
		{
			return (this.x1 <= x && x <= this.x2 && this.y1 <= y && y <= this.y2);
		}

		public override Url get_url (double x, double y)
		{
			if (this.url == null) {
				return null;
			}
	       //print (x, y), (this.x1, this.y1), "-", (this.x2, this.y2)
			if (this.is_inside (x, y)) {
				return new Url (this.url);
			}
			return null;
		}
	}
	
	public class Point {
		double x;
		double y;
		public Point(double x, double y) {
			this.x = x;
			this.y = y;
		}
	}
	
	public class Url {
		string url;
		public Url(string url) {
			this.url = url;
		}
	}
	
